num1=int(input("enter a num1 ="))
num2=int(input("enter a num2 ="))
symbol=input("enter a symbol =")
if symbol=="+":
    print(num1+num2)
elif symbol=="-":
    print(num1-num2)
elif symbol=="*":
    print(num1*num2)
elif symbol=="%":
    print(num1%num2)
elif symbol=="/":
    print(num1/num2)
elif symbol=="//":
    print(num1/num2)

else:
    print("nothing")


